palavra = input("digite algo")
print(f" O tipo primitivo de {palavra} é",type(palavra))
print(f"{palavra} só tem espaços?", palavra.isspace())
print(f"{palavra} só tem maiusculos?", palavra.isupper())
print(f"{palavra} só tem numeros?", palavra.isnumeric())
print(f"{palavra} é alfabetico?", palavra.isalpha())
print(f"{palavra} é alfanumerico?", palavra.isalnum())
print(f"{palavra} só tem minusculas?", palavra.islower())





